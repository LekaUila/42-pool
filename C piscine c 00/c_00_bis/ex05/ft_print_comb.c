#include <unistd.h>

void	ft_print_comb(void)
{
	char	n3;
	char	n2;
	char	n1;

	n1 = 47;
	while (++n1 < 58)
	{
		n2 = 47;
		while (++n2 < 58)
		{
			n3 = 47;
			while (++n3 < 58)
			{
				if (n3 > n2 && n2 > n1 && n3 > n1)
				{
					write(1, &n1, 1);
					write(1, &n2, 1);
					write(1, &n3, 1);
					if (!(n3 == 57 && n2 == 56 && n1 == 55 ))
						write(1, ", ", 2);
				}
			}
		}
	}
}
