#include <unistd.h>

void	ft_tempo(char n1, char n2, char n3, char n4)
{
	write(1, &n1, 1);
	write(1, &n2, 1);
	write(1, " ", 1);
	write(1, &n3, 1);
	write(1, &n4, 1);
	if (!(n1 == 57 && n2 == 56))
		write(1, ", ", 1);
}

void	ft_print_comb2(void)
{
	short int	n[4];

	n[0] = 48;
	n[1] = 48;
	n[2] = 48;
	n[3] = 48;
	while (!(n[1] == 57 && n[0] == 57))
	{	
		if (n[0] * 10 + n[1] < n[2] * 10 + n[3])
			ft_tempo(n[0], n[1], n[2], n[3]);
		if (++n[3] == 58)
		{
			n[3] = 48;
			if (++n[2] == 58)
			{
				n[2] = 48;
				if (++n[1] == 58)
					n[0]++;
				if (n[1] == 58)
					n[1] = 48;
			}
		}	
	}
}

int main(void)
{
	ft_print_comb2();	
	return (0);
}
