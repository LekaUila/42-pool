#include <unistd.h>

void	ft_print_num(int i)
{	
	if (i / 10 != 0)
		ft_print_num(i / 10);
	i = (i % 10) + 48;
	write(1, &i, 1);
}

void	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*str != 0)
	{
		i++;
		str++;
	}
	ft_print_num(i);
}
