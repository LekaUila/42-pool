char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	while (n != 0 && *(src + n - 1) == 0)
	{	
		*(dest + n - 1) = 0;
		n--;
	}
	n++;
	while (--n != 0)
	{
		*(dest + n - 1) = *(src + n - 1);
	}
	return (dest);
}
