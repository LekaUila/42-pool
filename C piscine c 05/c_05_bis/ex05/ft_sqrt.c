int	ft_sqrt(int nb)
{	
	int	ind;

	if (nb <= 0)
		return (0);
	ind = 0;
	while (ind * ind <= nb)
	{
		if (ind * ind == nb)
			return (ind);
		ind++;
	}
	return (0);
}
